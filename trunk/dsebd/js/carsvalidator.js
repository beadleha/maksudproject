function fixMakeSelection(doc)
{
	var str;
	if(doc[doc.selectedIndex].text=="All")
	{
		str = "<option value=''>All</option>";
		for (x in car_model_id)
		{
			str += "<option value='" + car_model_id[x] + "'>" + x+"</option>";
		}
	}
	else
	{
		var models1 = car_make_model[doc[doc.selectedIndex].text];
		
		str = "<option value=''>All</option>";
		for (x in models1)
		{
			str += "<option value='" + car_model_id[models1[x]] + "'>" + models1[x]+"</option>";
		}
	}
	document.getElementsByName("mdid")[0].innerHTML = str;
}

